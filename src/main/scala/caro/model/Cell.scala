package caro.model

case class Cell(tile: Option[String]) {
  def isOccupied:Boolean = tile.isDefined

  def putTile(color:String): Cell = if(!isOccupied) {
    copy(Some(color))
  } else this

  def getColor: String = {
    tile match {
      case Some(color) => color
      case None => "not occupied"
    }
  }
}

















































































































































































































































































































































































































































































































































































































































































































































